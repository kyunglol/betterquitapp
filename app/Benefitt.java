public class Benefitt {
    private String desc;
    private String sincelast;

    Benefitt(String desc , String sincelast){
        this.desc=desc;
        this.sincelast;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public void setSincelast(String sincelast) {
        this.sincelast = sincelast;
    }

    public String getDesc() {
        return desc;
    }

    public String getSincelast() {
        return sincelast;
    }
}
